function monkeyCount(n) {
    // Create empty array called monkeyCount
    let monkeyCount = []
    
    // Create for loop from 1 to n
    for (i = 1; i <= n; i++) {
      
      // For each iteration, add iterator to the end of the array monkeyCount
      monkeyCount.push(i)
      
    }
    
    // return monkeyCount
    return monkeyCount
  }